package net.project.utils;

/**
 * @author learnercys on 8/10/15.
 */
public class Point2D {
    private Integer x1, x2, y1, y2;

    public Point2D(Integer x1, Integer x2, Integer y1, Integer y2) {
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
    }

    public Integer getX1() {
        return x1;
    }

    public Integer getX2() {
        return x2;
    }

    public Integer getY1() {
        return y1;
    }

    public Integer getY2() {
        return y2;
    }
}
